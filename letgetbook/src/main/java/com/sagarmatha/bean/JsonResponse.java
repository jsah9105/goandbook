package com.sagarmatha.bean;

/**
 * @author jitendra
 *
 */
public class JsonResponse {
	
	private String message;
	private Long longmessage;
	private int intMessage;
	/**
	 * @return the message
	 */
	public String getMessage() {
		return message;
	}
	/**
	 * @param message the message to set
	 */
	public void setMessage(String message) {
		this.message = message;
	}
	/**
	 * @return the longmessage
	 */
	public Long getLongmessage() {
		return longmessage;
	}
	/**
	 * @param longmessage the longmessage to set
	 */
	public void setLongmessage(Long longmessage) {
		this.longmessage = longmessage;
	}
	/**
	 * @return the intMessage
	 */
	public int getIntMessage() {
		return intMessage;
	}
	/**
	 * @param intMessage the intMessage to set
	 */
	public void setIntMessage(int intMessage) {
		this.intMessage = intMessage;
	}
	

}
